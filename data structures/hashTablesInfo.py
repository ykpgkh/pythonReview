# Hash tables are a type of data structure in which the addresses or the index value of the data element is generated from a hash function. This makes accessing the data faster as the index value behaves as a key for the data value.

# To make it clear, hash tables store key-vlaue pairs but the key is generated through a hashing function

# in Python, the dictionary data types represent the implementation of hash tables. the keys in the dictionary satisfy the following requirements
#     - the keys of the dictionary are hashable 
#     - the order of data elements in a dictionary is NOT FIXED

